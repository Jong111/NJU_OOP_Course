#include <iostream>
using namespace std;

int main() {
    // 计算常见类型的大小
    cout << "bool 的大小是 " << sizeof(bool) << " 字节" << endl;
    cout << "char 的大小是 " << sizeof(char) << " 字节" << endl;
    cout << "int 的大小是 " << sizeof(int) << " 字节" << endl;
    cout << "float 的大小是 " << sizeof(float) << " 字节" << endl;
    cout << "double 的大小是 " << sizeof(double) << " 字节" << endl;
    cout << "long 的大小是 " << sizeof(long) << " 字节" << endl;
    cout << "long long 的大小是 " << sizeof(long long) << " 字节" << endl;
    cout << "unsigned 的大小是 " << sizeof(unsigned) << " 字节" << endl;


    return 0;
}









